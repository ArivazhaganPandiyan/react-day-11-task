/* 
Guvi Library Admin Dashboard Documentation
==========================================
Overview
========
The Guvi Library Admin Dashboard is a React-based web application designed to manage books and authors in a library setting. 
It provides functionalities for adding, editing, and deleting books and authors, as well as displaying detailed information about them.


Book Details
============
Title
=====
Represents the title of the book.
Required field.
Author
======
Specifies the author of the book.
Required field.
ISBN (International Standard Book Number)
========================================
Unique identifier for the book.
Required field.
Publication Date
================
Date when the book was published.
Optional field.

Author Details
==============
Name
====

Name of the author.
=================
Required field.
Birth Date
==========
Date of birth of the author.
Optional field.
Biography
=========
Brief description or biography of the author.
Optional field.

Features
========
Books Management
================

1.Add new books with details such as title, author, ISBN, and publication date.
2.Edit existing book details.
3.Delete books from the library database.
4.View a list of all books with their respective details.

Authors Management
=================
1.Add new authors with details such as name, birth date, and biography.
2.Edit existing author details.
3.Delete authors from the library database.
4.View a list of all authors with their respective details.

Usage
=====
Adding a Book
=============
Enter the title, author, ISBN, and publication date of the book in the respective input fields.
Click the "Add Book" button to add the book to the library.
Editing a Book
==============
Click the "Edit" button next to the book you want to edit.
Modify the book details in the input fields.
Click the "Update Book" button to save the changes.
Deleting a Book
===============
Click the "Delete" button next to the book you want to delete.
Confirm the deletion when prompted.
Adding an Author
===============
Enter the name, birth date, and biography of the author in the respective input fields.
Click the "Add Author" button to add the author to the library.
Editing an Author
=================
Click the "Edit" button next to the author you want to edit.
Modify the author details in the input fields.
Click the "Update Author" button to save the changes.
Deleting an Author
==================

Click the "Delete" button next to the author you want to delete.
Confirm the deletion when prompted.

Styling
======
The admin dashboard features a clean and user-friendly interface with responsive design elements. 
The use of tables enhances readability and organization of book and author information.

Dependencies
============
The dashboard utilizes Formik for form management and validation, enabling smooth user interactions when adding or editing books and authors.

Customization
=============
1.To customize the application, you can modify the styles in the App.css and LibraryDashBoard.css files.
2.You can also customize the LibraryDashBoard.jsx file to modify the components.

Built With
==========
React
JavaScript
HTML
CSS

Conclusion
The Guvi Library Admin Dashboard simplifies the management of library resources by providing an intuitive interface for administrators to add, edit, and delete books and authors. 
With its robust features and user-friendly design, it serves as an efficient tool for library management tasks.
*/